public class main { // Клас так обізваний, тому що в завданні треба було створити клас main, a не Main
    public static void main(String[] args) {
        int a = 3;
        int b = 5;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(++a + " " + ++b);
        System.out.println(--a + " " + --b);
        System.out.println(a++ + " " + b++);
        System.out.println(a-- + " " + b--);
        System.out.println(- a + " " + - b);
        System.out.println(+ a + " " + + b);
    }
}
